param(
  [string]$SmtpUser = "shichenghao321@gmail.com",
  [string]$DeliveryFrom = "CommerceFix <shichenghao321@gmail.com>"
)

$ErrorActionPreference = "Stop"

$projectRoot = Resolve-Path -LiteralPath (Join-Path $PSScriptRoot "..\..")
$EnvPath = Join-Path $projectRoot "01_app\.env.local"
$gitignorePath = Join-Path $projectRoot ".gitignore"

Write-Host "CommerceFix Gmail SMTP setup"
Write-Host "Target env file: $EnvPath"
Write-Host "The app password input will be hidden and will not be printed."

$securePassword = Read-Host "Paste NEW Gmail App Password" -AsSecureString
$bstr = [Runtime.InteropServices.Marshal]::SecureStringToBSTR($securePassword)
try {
  $plainPassword = [Runtime.InteropServices.Marshal]::PtrToStringBSTR($bstr)
} finally {
  [Runtime.InteropServices.Marshal]::ZeroFreeBSTR($bstr)
}

if ([string]::IsNullOrWhiteSpace($plainPassword)) {
  throw "SMTP app password cannot be empty."
}

$envDir = Split-Path -Parent $EnvPath
New-Item -ItemType Directory -Force -Path $envDir | Out-Null

$existing = @{}
if (Test-Path -LiteralPath $EnvPath) {
  Get-Content -LiteralPath $EnvPath | ForEach-Object {
    if ($_ -match '^\s*([^#][^=]+?)=(.*)$') {
      $existing[$matches[1].Trim()] = $matches[2]
    }
  }
}

$updates = [ordered]@{
  "SMTP_HOST" = "smtp.gmail.com"
  "SMTP_PORT" = "587"
  "SMTP_SECURE" = "false"
  "SMTP_USER" = $SmtpUser
  "SMTP_APP_PASSWORD" = $plainPassword
  "GMAIL_PROXY_URL" = "auto"
  "DELIVERY_FROM" = "`"$DeliveryFrom`""
  "DELIVERY_REPLY_TO" = $SmtpUser
  "IMAP_HOST" = "imap.gmail.com"
  "IMAP_PORT" = "993"
  "IMAP_SECURE" = "true"
  "IMAP_USER" = $SmtpUser
  "IMAP_APP_PASSWORD" = $plainPassword
  "IMAP_MAILBOX" = "INBOX"
  "IMAP_SEARCH_QUERY" = 'SUBJECT "CommerceFix CSV Request"'
  "IMAP_MAX_MESSAGES" = "10"
  "IMAP_DEFAULT_PLAN" = "Repair Pro"
}

foreach ($key in $updates.Keys) {
  $existing[$key] = $updates[$key]
}

$orderedKeys = @(
  "SMTP_HOST",
  "SMTP_PORT",
  "SMTP_SECURE",
  "SMTP_USER",
  "SMTP_APP_PASSWORD",
  "GMAIL_PROXY_URL",
  "DELIVERY_FROM",
  "DELIVERY_REPLY_TO",
  "IMAP_HOST",
  "IMAP_PORT",
  "IMAP_SECURE",
  "IMAP_USER",
  "IMAP_APP_PASSWORD",
  "IMAP_MAILBOX",
  "IMAP_SEARCH_QUERY",
  "IMAP_MAX_MESSAGES",
  "IMAP_DEFAULT_PLAN"
)

$lines = @(
  "# CommerceFix local-only email secrets",
  "# Do not commit this file.",
  "# Generated by set-gmail-smtp-secret.ps1."
)

foreach ($key in $orderedKeys) {
  if ($existing.ContainsKey($key)) {
    $lines += "$key=$($existing[$key])"
  }
}

foreach ($key in ($existing.Keys | Sort-Object)) {
  if ($orderedKeys -notcontains $key) {
    $lines += "$key=$($existing[$key])"
  }
}

Set-Content -LiteralPath $EnvPath -Value $lines -Encoding UTF8

if (Test-Path -LiteralPath $gitignorePath) {
  $gitignore = Get-Content -LiteralPath $gitignorePath -Raw
  if ($gitignore -notmatch '(^|\r?\n)01_app/\.env\.local(\r?\n|$)') {
    Add-Content -LiteralPath $gitignorePath -Value "`n01_app/.env.local"
  }
}

Write-Host "Saved SMTP and IMAP settings to .env.local."
Write-Host "Password length stored: $($plainPassword.Length) characters."
Write-Host "Password value was not printed."
